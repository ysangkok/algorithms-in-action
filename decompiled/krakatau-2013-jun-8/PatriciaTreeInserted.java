public class PatriciaTreeInserted {
    private String pattern;
    private int value;
    
    PatriciaTreeInserted(String s, int i)
    {
        super();
        this.pattern = s;
        this.value = i;
    }
    
    public String getPattern()
    {
        String s = this.pattern;
        return s;
    }
    
    public int getValue()
    {
        int i = this.value;
        return i;
    }
}