public class Inserted
{
    private String pattern;
    private int value;
    
    Inserted(final String pattern, final int value) {
        super();
        this.pattern = pattern;
        this.value = value;
    }
    
    public String getPattern() {
        return this.pattern;
    }
    
    public int getValue() {
        return this.value;
    }
}
